package com.olbius.sf.facebook;

public class FacebookSettings {
	private String appid;
	private String pageurl;
	private String facebokThemes;
	private String commentWidth;
	private String appSecret;
	private String supplierPage;
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getPageurl() {
		return pageurl;
	}
	public void setPageurl(String pageurl) {
		this.pageurl = pageurl;
	}
	public String getFacebokThemes() {
		return facebokThemes;
	}
	public void setFacebokThemes(String facebokThemes) {
		this.facebokThemes = facebokThemes;
	}
	public String getCommentWidth() {
		return commentWidth;
	}
	public void setCommentWidth(String commentWidth) {
		this.commentWidth = commentWidth;
	}
	public String getAppSecret() {
		return appSecret;
	}
	public void setAppSecret(String appSecret) {
		this.appSecret = appSecret;
	}
	public String getSupplierPage() {
		return supplierPage;
	}
	public void setSupplierPage(String supplierPage) {
		this.supplierPage = supplierPage;
	}
}
